                  },
              ),
              ),
              GestureDetector(//here for searching purpose
                onTap: () {
                 Navigator.pop(context,city_name);
                },
                child: Text(
                  'Get Weather',
                  style: kButtonTextStyle,
                ),
              ),
            ],
          ),
        ),
